public class StringExample6 {
public static void main(String[] args) {
	String s = "Rohit";
	s.toUpperCase();
	System.out.println(s);//Rohit
	s = s.toUpperCase();
	System.out.println(s);//ROHIT
	s.toLowerCase();
	System.out.println(s);
	s = s.toLowerCase();
	System.out.println(s);
	}

}
